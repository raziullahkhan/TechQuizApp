    private boolean validateInput(){
        char[] password=txtPassword.getPassword();
        char[] rePassword=txtRePassword.getPassword();
        if(password.length==0){
            txtRePassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(102, 102, 0), 1, true));
            lblBRePassword.setText("");
            txtPassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            lblBPassword.setText("Password can not left blank.");
            return false;
        }
        else if(rePassword.length==0){
            txtPassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(102, 102, 0), 1, true));
            lblBPassword.setText("");
            txtRePassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            lblBRePassword.setText("Retype password can not left blank.");
            return false;
        }
        else{
            txtRePassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(102, 102, 0), 1, true));
            lblBRePassword.setText("");
            txtPassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(102, 102, 0), 1, true));
            lblBPassword.setText("");
            if(!String.valueOf(password).equals(String.valueOf(rePassword))){
                txtRePassword.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
                lblBRePassword.setText("Passwords are not matching.");
                return false;
            }
        }
        pass=String.valueOf(password);
        return true;
     }
