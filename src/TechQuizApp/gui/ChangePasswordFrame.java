    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        if(!validateInput()){
            return;
        }
        try{
            boolean result=UserDAO.updatePassword(UserProfile.getUsername(), pass);
            if(!result){
                JOptionPane.showMessageDialog(null,"Password is not Changed!","Not Changed!",JOptionPane.ERROR_MESSAGE); 
                return;
            }
             JOptionPane.showMessageDialog(null,"Password is changed!","Changed!",JOptionPane.INFORMATION_MESSAGE);
             StudentOptionsFrame student=new StudentOptionsFrame();
             student.setVisible(true);
             this.dispose();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error!","Student Registration Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnChangeActionPerformed

